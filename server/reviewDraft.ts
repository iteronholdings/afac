const pick = <T,>(arr: T[]): T => arr[Math.floor(Math.random() * arr.length)];
const chance = (p: number) => Math.random() < p;
function shuffle<T>(arr: T[]): T[] {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

/** 캠페인 키워드/제목에서 자연스러운 상품 호칭을 뽑는다. */
function productNoun(keyword?: string | null, title?: string | null): string {
  const k = (keyword || "").trim();
  if (k && k.length <= 12) return k;
  const t = (title || "").trim();
  if (t) {
    const first = t.split(/[\s,/·\-(){}\[\]]+/).filter(Boolean)[0];
    if (first && first.length <= 12) return first;
  }
  return "제품";
}

// ── 한국어 조사 처리 ("홍삼로"→"홍삼으로" 같은 오류 방지) ──────
/** 단어 마지막 글자에 받침이 있는지. 한글 외(숫자/영문)는 발음 근사, 판단 불가면 null. */
function lastHasJong(word: string): boolean | null {
  const ch = word.trim().slice(-1);
  const c = ch.charCodeAt(0);
  if (c >= 0xac00 && c <= 0xd7a3) return (c - 0xac00) % 28 !== 0;
  if (/[0-9]/.test(ch)) return "013678".includes(ch); // 영·일·삼·육·칠·팔 = 받침 있음
  if (/[a-zA-Z]/.test(ch)) return /[bcdgkmnptlr]/i.test(ch); // 영문 끝소리 근사
  return null;
}

/** noun + 조사. pair는 "받침형/무받침형" (예: "이/가"). 으로/로는 ㄹ받침이면 '로'. */
function withJosa(noun: string, pair: "이/가" | "을/를" | "은/는" | "으로/로"): string {
  const [withJong, noJong] = pair.split("/");
  const j = lastHasJong(noun);
  if (j === null) return noun + noJong; // 판단 불가 → 무받침형(무난)
  if (pair === "으로/로" && j) {
    const c = noun.trim().slice(-1).charCodeAt(0);
    if (c >= 0xac00 && c <= 0xd7a3 && (c - 0xac00) % 28 === 8) return noun + "로"; // ㄹ받침 → 로
  }
  return noun + (j ? withJong : noJong);
}

/** 템플릿의 {N}/{N이}/{N을}/{N은}/{N으로}를 상품명+올바른 조사로 치환하는 함수 생성. */
function makeFill(noun: string) {
  return (s: string) =>
    s
      .replace(/\{N이\}/g, withJosa(noun, "이/가"))
      .replace(/\{N을\}/g, withJosa(noun, "을/를"))
      .replace(/\{N은\}/g, withJosa(noun, "은/는"))
      .replace(/\{N으로\}/g, withJosa(noun, "으로/로"))
      .replace(/\{N\}/g, noun);
}

