/** 글자 리뷰어 장문형 비율 (나머지는 단문형). 3:7 → 0.3 */
const LONG_FORM_RATIO = 0.3;

/** 먹는 상품(식품·건강식품)일 때 '쓰다' 계열 마무리를 '먹다' 계열로 교정. */
const EAT_CATEGORIES: Category[] = ["food", "health"];
const EAT_SWAPS: [string, string][] = [
  ["잘 쓰겠습니다", "잘 먹겠습니다"],
  ["오래 쓸 것 같습니다", "꾸준히 먹게 될 것 같습니다"],
  ["잘 쓸게요", "잘 먹을게요"],
  ["잘 씀.", "잘 먹음."],
  ["소중하게 쓸게요", "맛있게 먹을게요"],
  ["오래 곁에 두고 싶어요", "두고두고 생각날 것 같아요"],
  ["믿고 쓰는 걸로 정착할 것 같아요", "믿고 먹는 걸로 정착할 것 같아요"],
];

/** 오프너가 생략되는 단문에서도 키워드가 반드시 들어가도록 하는 리드 문장. */
const NOUN_LEADS = ["{N} 구매 후기예요.", "{N} 후기 남겨요.", "{N} 잘 받았어요."];

/** 사진 리뷰어용 장문 조립: 실제 리뷰 흐름의 7단 구성 (~700~900자). */
function composePhoto(persona: Persona, cat: Category): string[] {
  const quality = PHOTO_QUALITY[cat];
  const usage = PHOTO_USAGE[cat];
  const cons = cat === "food" ? [...CONS_CHUNKS, ...CONS_FOOD_EXTRA] : CONS_CHUNKS;
  return [
    pick(persona.openers),                       // ① 구매 동기
    pick(BG_EXTRA),                              //    + 배경 한마디
    pick(DELIVERY_CHUNKS),                       // ② 배송·포장
    pick(PHOTO_FIRST),                           // ③ 개봉 첫인상 + 사진 언급
    ...shuffle(quality).slice(0, 4),             // ④ 품질 상세 4덩이
    ...shuffle(usage).slice(0, 3),               // ⑤ 활용 3덩이
    pick(TRUST_CHUNKS),                          //    + 판매자·구성 신뢰
    pick(STORY_CHUNKS),                          //    + 지인·가족 반응
    pick(cons),                                  // ⑥ 아쉬운 점 살짝
    pick(AFTER_CHUNKS),                          //    + 며칠 지난 소감
    pick(persona.closers),                       // ⑦ 총평
    pick(CLOSING_EXTRA),                         //    + 재구매 마무리
  ];
}

/**
 * 한 편의 리뷰 원고를 생성한다.
 *  - 사진 리뷰어: 7단 구성 장문 (~700~900자) — 항상 최장.
 *  - 글자 리뷰어: 장문(2~3문장) 3 : 단문(1~2문장) 7 혼합 (~40~110자).
 * 이모지는 사용하지 않는다.
 */
export function generateReviewDraft(opts: {
  type: DraftType;
  title?: string | null;
  keyword?: string | null;
}): string {
  const noun = productNoun(opts.keyword, opts.title);
  const fill = makeFill(noun);
  const persona = pick(PERSONAS);
  const cat = detectCategory(opts.keyword, opts.title);

  let parts: string[];
  if (opts.type === "photo") {
    parts = composePhoto(persona, cat);
  } else {
    const long = Math.random() < LONG_FORM_RATIO;
    const bodyPool = [...CATEGORY_BODIES[cat], ...COMMON_BODIES];
    const bodyCount = long ? (chance(0.5) ? 2 : 3) : (chance(0.5) ? 1 : 2);
    parts = [];
    if (long || !chance(0.35)) parts.push(pick(persona.openers)); // 단문은 가끔 오프너 생략
    parts.push(...shuffle(bodyPool).slice(0, bodyCount), pick(persona.closers));
  }

  let text = parts.map(fill).join(" ");

  // 검색 키워드가 본문에 반드시 들어가도록 보장 (상위노출 리뷰의 핵심).
  if (!text.includes(noun)) {
    text = `${fill(pick(NOUN_LEADS))} ${text}`;
  }

  // 먹는 상품이면 '잘 쓰겠습니다' 같은 어색한 마무리를 '먹다' 계열로 교정.
  if (EAT_CATEGORIES.includes(cat)) {
    for (const [from, to] of EAT_SWAPS) text = text.split(from).join(to);
  }

  return text;
}
