/** 장문형 비율 (나머지는 단문형). 3:7 → 0.3 */
const LONG_FORM_RATIO = 0.3;

/** 먹는 상품(식품·건강식품)일 때 '쓰다' 계열 마무리를 '먹다' 계열로 교정. */
const EAT_CATEGORIES: Category[] = ["food", "health"];
const EAT_SWAPS: [string, string][] = [
  ["잘 쓰겠습니다", "잘 먹겠습니다"],
  ["오래 쓸 것 같습니다", "꾸준히 먹게 될 것 같습니다"],
  ["잘 쓸게요", "잘 먹을게요"],
  ["잘 씀.", "잘 먹음."],
  ["소중하게 쓸게요", "맛있게 먹을게요"],
  ["오래 곁에 두고 싶어요", "두고두고 생각날 것 같아요"],
  ["효율 좋네요", "맛도 가격도 좋네요"],
  ["육아템으로 인정!", "아이 간식으로 인정!"],
];

/** 오프너가 생략되는 단문에서도 키워드가 반드시 들어가도록 하는 리드 문장. */
const NOUN_LEADS = ["{N} 구매 후기예요.", "{N} 후기 남겨요.", "{N} 잘 받았어요."];

/**
 * 한 편의 리뷰 원고를 생성한다. (페르소나 무작위 → 말투 다양화 + 길이 랜덤화)
 *  - 사진 리뷰어: 항상 최장 — 오프너 + 본문 4~5문장 + 실물언급 + 클로저 (7~8문장).
 *  - 글자 리뷰어: LONG_FORM_RATIO(3:7)로 장문(3~4문장)/단문(1~2문장, 가끔 오프너 생략) 혼합.
 * 본문은 상품 카테고리 풀(키워드 문장 포함)에서, 말투는 페르소나 오프너·클로저가 담당.
 */
export function generateReviewDraft(opts: {
  type: DraftType;
  title?: string | null;
  keyword?: string | null;
}): string {
  const noun = productNoun(opts.keyword, opts.title);
  const fill = makeFill(noun);
  const persona = pick(PERSONAS);
  // 사진 리뷰어는 항상 장문(가장 길게). 글자 리뷰어만 장:단 = 3:7 혼합.
  const isPhoto = opts.type === "photo";
  const long = isPhoto ? true : Math.random() < LONG_FORM_RATIO;

  // 본문 풀: 감지된 카테고리 본문 + 공통(배송·가성비). 미감지 상품은 중립 본문(default).
  // (페르소나 본문은 용도를 전제해 상품과 어긋날 수 있어 사용하지 않는다 — 말투는 오프너·클로저가 담당)
  const cat = detectCategory(opts.keyword, opts.title);
  const bodyPool = [...CATEGORY_BODIES[cat], ...COMMON_BODIES];

  // 본문 문장 수: 사진 5~6(항상 최장), 글자 장문 2~3, 글자 단문 1~2.
  // 사진(오프너+본문5~6+실물언급+클로저=8~9파트)이 글자(최대 6파트)보다 항상 길다.
  const bodyCount = isPhoto ? (chance(0.5) ? 5 : 6) : long ? (chance(0.5) ? 2 : 3) : (chance(0.5) ? 1 : 2);
  const body = shuffle(bodyPool).slice(0, bodyCount);

  // 사진형은 실물/사진 언급을 항상 포함 (사진과 함께 올라가는 리뷰).
  if (isPhoto) {
    body.splice(chance(0.5) ? 0 : body.length, 0, pick(PHOTO_LINES));
  }

  const parts: string[] = [];
  // 글자 단문은 가끔 오프너를 생략해 더 짧게. (사진은 항상 오프너 포함)
  if (long || !chance(0.35)) parts.push(pick(persona.openers));
  parts.push(...body, pick(persona.closers));
  let text = parts.map(fill).join(" ");

  // 검색 키워드가 본문에 반드시 들어가도록 보장 (상위노출 리뷰의 핵심).
  if (!text.includes(noun)) {
    text = `${fill(pick(NOUN_LEADS))} ${text}`;
  }

  // 먹는 상품이면 '잘 쓰겠습니다' 같은 어색한 마무리를 '먹다' 계열로 교정.
  if (EAT_CATEGORIES.includes(cat)) {
    for (const [from, to] of EAT_SWAPS) text = text.split(from).join(to);
  }

  const emoji = pick(persona.emojis);
  if (emoji) text += ` ${emoji}`;

  return text;
}
